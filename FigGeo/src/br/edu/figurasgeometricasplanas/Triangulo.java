package br.edu.figurasgeometricasplanas;

public class Triangulo {

//atributos
public double base;
public double altura;
public double area;

//construtor
public Triangulo(){
base = 0;
altura = 0;
area = 0;
}

//metodos
public void calcArea(){
area = (base*altura)/2;	
}
                      }
